    <article class="articulo_1"> <p>¿Cómo se reproducen los mosquitos?</p>El Aedes aegypti tiene distintas etapas de desarrollo: los huevos, las larvas, las pupas (etapas que transcurren en el agua) y los mosquitos adultos (cuando ya pueden volar).
Se crían en lugares sombríos y húmedos. Los sitios oscuros aseguran que el agua de los recipientes no sobrepase ciertas temperaturas que serían letales para los huevos, larvas y pupas. Cuando son adultos requieren de humedad para sobrevivir mayor tiempo, así los pastos altos son un ambiente ideal que ofrece alimento y refugio del Aedes aegypti.
Cualquier recipiente capaz de acumular agua puede convertirse en un criadero pero algunos pueden producir gran cantidad de mosquitos.
Para más información, haga <a href="https://www.argentina.gob.ar/salud/mosquitos/hogar#:~:text=El%20Aedes%20aegypti%20tiene%20distintas,en%20lugares%20sombr%C3%ADos%20y%20h%C3%BAmedos"><mark>click aquí</a></mark>
</article>

<article class="articulo_2"> <p>Información general del mosquito Aedes aegypti:</p>
<li>Se cría en recipientes con agua acumulada en los alrededores del hogar.<br> </li>
<li>Pone sus huevos en las paredes de envases con agua, donde pueden sobrevivir por meses y nacen al ser sumergidos bajo agua. <br></li>
<li>El ciclo de vida del huevo a larva, a pupa y luego a mosquito adulto volador es de 8 días y ocurre en el agua. El mosquito adulto puede vivir hasta un mes.<br></li>
<li>Los mosquitos reposan usualmente dentro de las casas (closets, detrás de muebles, cortinas) y las hembras son las que pican.<br></li>
<li>Son capaces de volar cientos de metros buscando recipientes donde colocar sus huevos. <br></li>
<li>Unos cuantos mosquitos por casa son capaces de producir grandes epidemias de dengue. <br></li>
</article>

<article class="articulo_3"><p>Los recipientes comunes en donde se crían los mosquitos del dengue son: </p>
Aquellos que son llenados con agua por las personas:tanques de agua, envases de pintura de 5 galones, cubos o baldes pequeños, bebederos de animales, piscinas plásticas que no se usan, entre otros muchos más ejemplos.<br>
Aquellos que se llenan con las lluvias: Envases desechados como gomas (neumáticos), botellas, ollas, artefactos dañados (inodoros, lavamanos, neveras, lavadoras, etc.).<br>
